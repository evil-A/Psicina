int	ft_str_is_lowercase(char *str)
{
	int	flag;

	flag = 1;
	while (*str != '\0')
	{
		if (*str < 'a' || *str > 'z')
		{
			flag = flag * 0;
		}
		str++;
	}
	return (flag);
}
