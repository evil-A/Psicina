void	conditions(int x, int y, int c, int l)
{
	if ((c == 0 && l == 0) || (c == 0 && l == (y - 1)))
	{
		ft_putchar('A');
	}
	else if ((c == (x - 1) && l == 0) || (c == (x - 1) && (l == (y - 1))))
	{
		ft_putchar('C');
	}
	else if (l != 0 && l != (y - 1) && c != 0 && c != (x - 1))
	{
		ft_putchar(' ');
	}
	else
	{
		ft_putchar('B');
	}
}
