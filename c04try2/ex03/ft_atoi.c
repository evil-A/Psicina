int	ft_atoi(char *str)
{
	int	i;
	int ret;
	int nbr;

	i = 0;
	ret = 1;
	nbr = 0;
	while ((str[i] == ' ') || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			ret = ret * (-1);
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nbr = (nbr * 10) + (str[i] - '0');
		i++;
	}
	nbr = nbr * ret;
	return (nbr);
}
