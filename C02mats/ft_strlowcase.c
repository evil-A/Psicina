char	*ft_strupcase(char *str)
{
	while (*str != '\0')
	{
		if (*str >= 'A' && *str <= 'Z')
		{
			*str = *str + 32;
		}
		else
		{
			*str = *str;
		}
		str++;
	}
	*str = '\0';
	return (str);
}
